public class Contato {
    String nome;
    String email;
    String telefone;
    String dataNasc;

    Contato(String nome, String email, String telefone, String dataNasc)
    {
        this.nome = nome;
        this.email = email;
        this.telefone = telefone;
        this.dataNasc = dataNasc;
    }
}
